function toRoman(n) {
  var map = [[1000,"M"],[900,"CM"],[500,"D"],[400,"CD"],[100,"C"],[90,"XC"],[50,"L"],[40,"XL"],[10,"X"],[9,"IX"],[5,"V"],[4,"IV"],[1,"I"]];
  var r = "";
  for (var i = 0; i < map.length; i++) { while (n >= map[i][0]) { r += map[i][1]; n -= map[i][0]; } }
  return r;
}
function saveState() { try { sessionStorage.setItem("seven_nights_state", JSON.stringify(state)); } catch(e) {} }
function loadState() {
  try {
    var s = sessionStorage.getItem("seven_nights_state");
    if (s) { var p = JSON.parse(s); state.currentScene = p.currentScene || "ch1-evening"; state.flags = p.flags || {}; state.inventory = p.inventory || []; state.steps = p.steps || 0; state.path = p.path || []; }
  } catch(e) {}
}
function renderScene(id) {
  var s = scenes[id]; if (!s) { document.getElementById("game-desc").innerHTML = "<p style='color:var(--text-dim);'>没有更多了</p>"; return; }
  document.getElementById("game-container").className = "mood-" + s.mood;
  document.getElementById("game-title").textContent = s.title;
  document.getElementById("game-desc").innerHTML = s.desc.replace(/\n/g, "<br>");
  var ne = document.getElementById("game-note");
  if (s.descAfter) { ne.textContent = s.descAfter; ne.style.display = ""; } else { ne.style.display = "none"; }
  document.getElementById("game-status").textContent = "—— " + s.title + " ——";
  var cel = document.getElementById("game-choices");
  var f = s.choices.filter(function(c) {
    if (c.if) { var neg = c.if[0] === "!"; var fn = neg ? c.if.slice(1) : c.if; var hf = !!state.flags[fn]; return neg ? !hf : hf; }
    if (c.needItem) return state.inventory.indexOf(c.needItem) !== -1;
    return true;
  });
  cel.innerHTML = f.map(function(c,i) {
    return '<button class="choice-line" data-to="' + c.to + '"' + (c.setFlag ? ' data-flag="' + c.setFlag + '"' : '') + (c.addItem ? ' data-item="' + c.addItem + '"' : '') + (c.reset ? ' data-reset="true"' : '') + '><span class="choice-numeral">' + toRoman(i+1) + '.</span>' + c.text + '</button>';
  }).join("");
  if (s.isEnding) document.getElementById("game-container").className += " is-ending";
}
function navigateTo(id, el) {
  if (el) {
    var f = el.getAttribute("data-flag"); if (f) state.flags[f] = true;
    var it = el.getAttribute("data-item"); if (it && state.inventory.indexOf(it) === -1) state.inventory.push(it);
    var r = el.getAttribute("data-reset");
    if (r === "true") { state.currentScene = id; state.flags = {}; state.inventory = []; state.steps = 0; state.path = []; saveState(); renderScene(id); return; }
  }
  state.currentScene = id; state.steps++; state.path.push(id); saveState(); renderScene(id);
}
